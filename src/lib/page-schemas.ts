export function getBreadcrumbs(path: string, pageName: string) {
  const parts = path.split("/").filter(Boolean);
  const breadcrumbs = [{ name: "Home", url: "/" }];
  
  if (parts.length > 0 && parts[0] !== "services" && parts[0] !== "products") {
    breadcrumbs.push({ name: parts[0].charAt(0).toUpperCase() + parts[0].slice(1), url: `/${parts[0]}` });
  }
  
  if (parts[0] === "services") {
    breadcrumbs.push({ name: "Services", url: "/services" });
  } else if (parts[0] === "products") {
    breadcrumbs.push({ name: "Products", url: "/products" });
  }
  
  breadcrumbs.push({ name: pageName, url: path });
  
  return breadcrumbs;
}

